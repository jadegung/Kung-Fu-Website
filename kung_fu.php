  <h2 class="yellow"> Dragon Style Kung Fu </h2></center></strong>
<p>Dragon Style Kung Fu was one of the five original systems of Chinese boxing that originated from the Shaolin Temple. Lam Yew Kwai was the first member of the public to learn this esoteric art from Monk Tai Yuk Sim See during the late 1800s.  Grandmaster Lam taught the system to individuals throughout China and in British Hong Kong. One of his students is Cho Sam who studied both Bak Mei (White Eyebrow) and Dragon Style Kung Fu. Grandmaster Cho would teach the complete Dragon Style Kung Fu system to Sifu Yip Wing Hong. Sifu Yip is one of the first individuals to spread the art in the United States.</p>
<p>Dragon Style Kung Fu is a defensive and offensive combat system utilizing various hand techniques and low   kicks that strikes and traps the opponent. The dragon stylist employs a series of swift footwork, punches, grabs, locks, open handed strikes and kicks. The dragon stylist does not use brute force and muscular strength but develops sound waist power and applies similar Tai Chi principles.</p>
<p>Training in Dragon Style Kung Fu is rigorous but rewarding. It involves developing powerful strikes and an awareness of the entire body for swiftness in defending and countering a sudden attack. Advancement is based solely on individual merit. There are no colored sashes or upgrading tests. Since Dragon Style Kung Fu is rarely taught in North America, few have have seen this rare blend of both external and internal powers. </p>
